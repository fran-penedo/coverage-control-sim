/**
 * Interface mode. Mainly used to change behaviour of mouse events on the
 * tableau.
 * 
 * @author Francisco Penedo (franp@bu.edu)
 *
 */
public enum Mode {
	/**
	 * Select actor mode.
	 */
	SELECT,
	/**
	 * Add agents mode.
	 */
	ADD_AGENT,
	/**
	 * Add targets mode.
	 */
	ADD_TARGET,
	/**
	 * Add obstacles mode.
	 */
	ADD_OBST
}
